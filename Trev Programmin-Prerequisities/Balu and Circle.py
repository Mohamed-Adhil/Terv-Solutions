'''
Balu and Circle
Balu is a boy who likes maths. One day in his maths class his maths teacher thought about circles and he wanted to try finding the area of it. He chose the points randomly and started finding the area. He wants to do it without any errors. But has trouble finding the area. Help him find the Area.

Input Format : The input contains an integer r-the radius.

Input Constraints : 1<=r<=10^5

Output Format : Print the area of circle.
Note:Print two digit after the decimal point.

Sample Input :

5
Sample Output :

78.50
'''

n = int(input())
a = 3.14*n*n
print('%0.2f' % a)
